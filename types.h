/****************************************************************************
 *  Data Types Definition                                                   *
 *  Change these for portability across compilers/architectures             *
 ****************************************************************************/

#ifndef __TYPES_H__
#define __TYPES_H__


typedef unsigned char   UINT8;      // 8-bit unsigned (0-255)
typedef signed char     INT8;       // 8-bit signed (-127 to +127)
typedef unsigned int    UINT16;     // 16-bit unsigned (0-65535)
typedef signed int      INT16;      // 16-bit signed (-32767 to +32767)
typedef unsigned long   UINT32;     // 32-bit unsigned
typedef signed long     INT32;      // 32-bit signed

#endif /* __TYPES_H__ */
